    private static Random rnd;

    private static Random Rnd
    {
      get
      {
        if (rnd == null)
          rnd = new Random();

        return rnd;
      }
    }

    internal static string GetText()
    {
      int sentences = Rnd.Next(20);
      return GetText(sentences);
    }

    internal static string GetText(int sentences)
    {
      StringBuilder builder = new StringBuilder();
      int firstSentenceSize = Rnd.Next(10)+5;

      builder.Append(GetFirstSentence(firstSentenceSize));

      for (int i = 0; i < sentences; i++)
      {
        int words = Rnd.Next(10)+5;
        int appendStyle = Rnd.Next(10);
        string sentence = GetSentence(words);

        if (appendStyle > 8)
          builder.AppendLine("    "+sentence);
        else
          builder.Append(" " + sentence);
      }

      return builder.ToString();
    }

    internal static string GetFirstSentence(int numberOfWords)
    {
      string lorem = "lorem ipsum dolor sit amet";
      return GetSentence(lorem, numberOfWords+2);
    }

    internal static string GetSentence(int numberOfWords)
    {
      StringBuilder builder = new StringBuilder();
      for (int i = 0; i < numberOfWords; i++)
      {
        int wordIndex = Rnd.Next(words.Length);
        builder.Append(words[wordIndex]);

        int comaIndex = Rnd.Next(100);
        if (comaIndex > 94)
          builder.Append(", ");
        else
          builder.Append(" ");
      }


      int ptIndex = Rnd.Next(12);
      switch (ptIndex)
      {
        case 0:
        case 1: builder.Append("?");
          break;
        case 2: builder.Append("!");
          break;
        default: builder.Append(".");
          break;
      }

      string result = builder.ToString();

      result = FormatSentence(result);
      return result;
    }

    private static string FormatSentence(string result)
    {
      result = result[0].ToString().ToUpper() + result.Substring(1);
      return result;
    }

    private static string GetSentence(string start, int numberOfWords)
    {
      string sentence = start + GetSentence(numberOfWords);
      sentence = FormatSentence(sentence.ToLower());
      return sentence;
    }

